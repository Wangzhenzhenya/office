   什么是价值

毕加索生前在一张报纸空白处画的速写，居然拍卖到十几万美金。

“这速写不过草草几笔，根本没什么艺术价值。”有评论家说，“有的话，只是市场价值。”

20世纪10年代的中国，政经混乱、通货膨胀，百元当十元用，千元当百元用，买个鸡蛋也要上万元。“这钱还有什么钱的价值？”有人骂，“它还不如纸的价值。”

